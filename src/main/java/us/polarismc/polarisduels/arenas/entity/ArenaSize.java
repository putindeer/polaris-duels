package us.polarismc.polarisduels.arenas.entity;

/**
 * Represents the size classification of an arena.
 * This enum is used to categorize arenas based on their physical dimensions and capacity.
 */
public enum ArenaSize {
    /** Small-sized arena, typically for matches without block modification */
    SMALL,
    
    /** Medium-sized arena, suitable for matches with block modification */
    MEDIUM,
    
    /** Large-sized arena, designed for larger team battles or free-for-alls */
    LARGE
}
